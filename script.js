// Array creation
const fruits = ['apple','banana','cherry'];
console.log(fruits)

// Accessing Elements
console.log(fruits[0])
console.log(fruits[2])

// Adding Elements
fruits.push("orange");
console.log(fruits);

// Removing Elements
fruits.pop();  // Removes the last element
console.log(fruits);

// Lenghth of an Array
console.log(fruits.length); // Number of Elements